package com.EshaApplication.Dayfour;

/*
 * Flow control : Iterative statement
 * do-while loop
 * */
public class DoWhile {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < 5);
	}

}